package writer

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

func TestIsGeneratedFileRecognizesHeadersAndLockJSON(t *testing.T) {
	if !IsGeneratedFile([]byte("// Code generated by goweb-scaffold. DO NOT EDIT.\npackage demo\n")) {
		t.Fatalf("expected generated header to be recognized")
	}
	if !IsGeneratedFile([]byte("{\"generated_by\":\"goweb-scaffold\",\"module_name\":\"demo\"}\n")) {
		t.Fatalf("expected generator lock json to be recognized")
	}
	if IsGeneratedFile([]byte("package demo\n")) {
		t.Fatalf("did not expect handwritten file to be recognized")
	}
}

func TestWriteRejectsUnsafePathAndHandwrittenFile(t *testing.T) {
	root := t.TempDir()
	w := New(root)

	if _, _, err := w.Write("../escape.txt", []byte("x"), true); err == nil {
		t.Fatalf("expected unsafe path write to fail")
	}

	target := filepath.Join(root, "demo.txt")
	if err := os.WriteFile(target, []byte("handwritten"), 0o644); err != nil {
		t.Fatalf("write handwritten file: %v", err)
	}
	status, warning, err := w.Write("demo.txt", []byte(GeneratedHeader+"\npackage demo\n"), true)
	if err != nil {
		t.Fatalf("write over handwritten file: %v", err)
	}
	if status != "skipped" {
		t.Fatalf("expected handwritten file write to skip, got %s", status)
	}
	if !strings.Contains(warning, "not generator-managed") {
		t.Fatalf("expected handwritten warning, got %q", warning)
	}
}

func TestDeleteOnlyRemovesGeneratorManagedFiles(t *testing.T) {
	root := t.TempDir()
	w := New(root)

	generatedPath := filepath.Join(root, "managed.go")
	if err := os.WriteFile(generatedPath, []byte(GeneratedHeader+"\npackage demo\n"), 0o644); err != nil {
		t.Fatalf("write generated file: %v", err)
	}
	status, warning, err := w.Delete("managed.go")
	if err != nil {
		t.Fatalf("delete generated file: %v", err)
	}
	if status != "removed" || warning != "" {
		t.Fatalf("expected generated delete success, got status=%s warning=%q", status, warning)
	}

	lockPath := filepath.Join(root, "lock.json")
	if err := os.WriteFile(lockPath, []byte("{\"generated_by\":\"goweb-scaffold\"}\n"), 0o644); err != nil {
		t.Fatalf("write lock file: %v", err)
	}
	status, warning, err = w.Delete("lock.json")
	if err != nil {
		t.Fatalf("delete lock file: %v", err)
	}
	if status != "removed" || warning != "" {
		t.Fatalf("expected lock delete success, got status=%s warning=%q", status, warning)
	}

	handwrittenPath := filepath.Join(root, "manual.go")
	if err := os.WriteFile(handwrittenPath, []byte("package demo\n"), 0o644); err != nil {
		t.Fatalf("write handwritten file: %v", err)
	}
	status, warning, err = w.Delete("manual.go")
	if err != nil {
		t.Fatalf("delete handwritten file: %v", err)
	}
	if status != "skipped" || !strings.Contains(warning, "not generator-managed") {
		t.Fatalf("expected handwritten delete to skip, got status=%s warning=%q", status, warning)
	}
}
