package writer

import (
	"bytes"
	"fmt"
	"os"
	"path/filepath"
	"strings"
)

const GeneratedMarker = "Code generated by goweb-scaffold. DO NOT EDIT."

const GeneratedHeader = "// " + GeneratedMarker

type SafeWriter struct {
	root string
}

func New(root string) *SafeWriter {
	return &SafeWriter{root: filepath.Clean(root)}
}

func (w *SafeWriter) Root() string {
	return w.root
}

func (w *SafeWriter) Write(relPath string, content []byte, overwrite bool) (status string, warning string, err error) {
	targetPath, err := w.safePath(relPath)
	if err != nil {
		return "", "", err
	}

	if err := os.MkdirAll(filepath.Dir(targetPath), 0o755); err != nil {
		return "", "", err
	}

	existing, readErr := os.ReadFile(targetPath)
	switch {
	case readErr == nil:
		isGenerated := IsGeneratedFile(existing)
		if !isGenerated {
			return "skipped", fmt.Sprintf("%s exists and is not generator-managed", relPath), nil
		}
		if bytes.Equal(existing, content) {
			return "skipped", "", nil
		}
		if !overwrite {
			return "skipped", fmt.Sprintf("%s already exists; set overwrite=true to rewrite", relPath), nil
		}
		if err := os.WriteFile(targetPath, content, 0o644); err != nil {
			return "", "", err
		}
		return "overwritten", "", nil
	case !os.IsNotExist(readErr):
		return "", "", readErr
	default:
		if err := os.WriteFile(targetPath, content, 0o644); err != nil {
			return "", "", err
		}
		return "generated", "", nil
	}
}

func IsGeneratedFile(content []byte) bool {
	trimmed := strings.TrimSpace(string(content))
	if trimmed == "" {
		return false
	}
	firstLine := strings.SplitN(trimmed, "\n", 2)[0]
	return strings.Contains(firstLine, GeneratedMarker)
}

func (w *SafeWriter) safePath(relPath string) (string, error) {
	cleanRel := filepath.Clean(relPath)
	if cleanRel == "." || strings.HasPrefix(cleanRel, "..") || filepath.IsAbs(cleanRel) {
		return "", fmt.Errorf("unsafe relative path: %s", relPath)
	}

	target := filepath.Clean(filepath.Join(w.root, cleanRel))
	rootWithSep := w.root + string(filepath.Separator)
	if target != w.root && !strings.HasPrefix(target, rootWithSep) {
		return "", fmt.Errorf("target path escapes repo root: %s", relPath)
	}
	return target, nil
}
