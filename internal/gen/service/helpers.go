func ToPascal(raw string) string {
	parts := strings.FieldsFunc(raw, func(r rune) bool {
		return r == '_' || r == '-' || r == ' '
	})
	builder := strings.Builder{}
	for _, part := range parts {
		if part == "" {
			continue
		}
		builder.WriteString(strings.ToUpper(part[:1]))
		if len(part) > 1 {
			builder.WriteString(part[1:])
		}
	}
	return builder.String()
}

func ToKebab(raw string) string {
	return strings.ReplaceAll(strings.TrimSpace(raw), "_", "-")
}

func ToSnake(raw string) string {
	return strings.ReplaceAll(strings.TrimSpace(raw), "-", "_")
}

func HumanizeModuleName(raw string) string {
	parts := strings.FieldsFunc(strings.TrimSpace(raw), func(r rune) bool {
		return r == '_' || r == '-'
	})
	for index, part := range parts {
		if part == "" {
			continue
		}
		parts[index] = strings.ToUpper(part[:1]) + part[1:]
	}
	return strings.Join(parts, " ")
}

func GoFieldName(column string) string {
	parts := strings.FieldsFunc(strings.TrimSpace(column), func(r rune) bool {
		return r == '_' || r == '-' || r == ' '
	})
	builder := strings.Builder{}
	for _, part := range parts {
		if part == "" {
			continue
		}
		lower := strings.ToLower(part)
		switch lower {
		case "id":
			builder.WriteString("ID")
		case "ids":
			builder.WriteString("IDs")
		case "ip":
			builder.WriteString("IP")
		case "url":
			builder.WriteString("URL")
		case "api":
			builder.WriteString("API")
		case "json":
			builder.WriteString("JSON")
		default:
			builder.WriteString(strings.ToUpper(part[:1]))
			if len(part) > 1 {
				builder.WriteString(part[1:])
			}
		}
	}
	return builder.String()
}

func GoTypeForColumn(column ColumnInfo) string {
	switch {
	case column.IsPrimaryKey:
		return "int64"
	case isBigIntegerType(column.DataType):
		return "int64"
	case isIntegerType(column.DataType):
		return "int"
	case isBooleanType(column.DataType):
		return "bool"
	case isTimestampType(column.DataType):
		if column.IsNullable {
			return "*time.Time"
		}
		return "time.Time"
	case strings.EqualFold(strings.TrimSpace(column.DataType), "jsonb"):
		return "datatypes.JSON"
	default:
		return "string"
	}
}

func RequestTypeForColumn(column ColumnInfo) string {
	switch {
	case isBigIntegerType(column.DataType):
		return "int64"
	case isIntegerType(column.DataType):
		return "int"
	case isBooleanType(column.DataType):
		return "bool"
	case isTimestampType(column.DataType):
		return "string"
	case strings.EqualFold(strings.TrimSpace(column.DataType), "jsonb"):
		return "json.RawMessage"
	default:
		return "string"
	}
}

func DefaultTSValue(field SchemaField, columnIndex map[string]ColumnInfo) string {
	column, ok := columnIndex[field.Field]
	if !ok {
		return "''"
	}
	switch {
	case isBooleanType(column.DataType):
		return "false"
	case isIntegerType(column.DataType):
		return "0"
	case strings.EqualFold(strings.TrimSpace(column.DataType), "jsonb"):
		return "''"
	default:
		return "''"
	}
}

func MarshalIndent(value any) string {
	raw, err := json.MarshalIndent(value, "", "  ")
	if err != nil {
		return "[]"
	}
	return string(raw)
}

func uniqueStrings(values []string) []string {
	result := make([]string, 0, len(values))
	seen := make(map[string]struct{}, len(values))
	for _, value := range values {
		item := strings.TrimSpace(value)
		if item == "" {
			continue
		}
		if _, ok := seen[item]; ok {
			continue
		}
		seen[item] = struct{}{}
		result = append(result, item)
	}
	return result
}

func sortedUniqueStrings(values []string) []string {
	result := uniqueStrings(values)
	sort.Strings(result)
	return result
}

func isBigIntegerType(dataType string) bool {
	return strings.EqualFold(strings.TrimSpace(dataType), "bigint")
}

func isIntegerType(dataType string) bool {
	switch strings.ToLower(strings.TrimSpace(dataType)) {
	case "integer", "smallint":
		return true
	default:
		return false
	}
}

func isBooleanType(dataType string) bool {
	return strings.EqualFold(strings.TrimSpace(dataType), "boolean")
}

func isTimestampType(dataType string) bool {
	switch strings.ToLower(strings.TrimSpace(dataType)) {
	case "timestamp with time zone", "timestamp without time zone", "timestamp", "timestamptz":
		return true
	default:
		return false
	}
}

