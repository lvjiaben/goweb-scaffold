package registry

import (
	"os"
	"path/filepath"
	"strings"
	"testing"
)

func writeMetaFile(t *testing.T, root string, moduleName string, routePath string, pageName string, title string) {
	t.Helper()
	dir := filepath.Join(root, "internal/modules", moduleName)
	if err := os.MkdirAll(dir, 0o755); err != nil {
		t.Fatalf("mkdir %s: %v", dir, err)
	}
	content := `// Code generated by goweb-scaffold. DO NOT EDIT.
package ` + moduleName + `

const (
	GeneratedModuleName = "` + moduleName + `"
	GeneratedTableName  = "` + moduleName + `"
	GeneratedRoutePath  = "` + routePath + `"
	GeneratedPageName   = "` + pageName + `"
	GeneratedMenuTitle  = "` + title + `"
)
`
	if err := os.WriteFile(filepath.Join(dir, "meta.go"), []byte(content), 0o644); err != nil {
		t.Fatalf("write meta.go: %v", err)
	}
}

func TestRenderRegistryFilesStableAndSorted(t *testing.T) {
	root := t.TempDir()
	if err := os.MkdirAll(filepath.Join(root, "internal/modules"), 0o755); err != nil {
		t.Fatalf("mkdir modules: %v", err)
	}
	writeMetaFile(t, root, "zeta_article", "/system/zeta-article", "ZetaArticlePage", "Zeta")
	writeMetaFile(t, root, "alpha_article", "/system/alpha-article", "AlphaArticlePage", "Alpha")

	firstBackend, err := RenderBackendModulesFile(root)
	if err != nil {
		t.Fatalf("render backend modules first: %v", err)
	}
	secondBackend, err := RenderBackendModulesFile(root)
	if err != nil {
		t.Fatalf("render backend modules second: %v", err)
	}
	if string(firstBackend) != string(secondBackend) {
		t.Fatalf("expected backend registry output to be stable")
	}
	if strings.Index(string(firstBackend), "alpha_article") > strings.Index(string(firstBackend), "zeta_article") {
		t.Fatalf("expected backend registry output to be sorted, got:\n%s", string(firstBackend))
	}

	firstRoutes, err := RenderAdminRoutesFile(root)
	if err != nil {
		t.Fatalf("render admin routes first: %v", err)
	}
	secondRoutes, err := RenderAdminRoutesFile(root)
	if err != nil {
		t.Fatalf("render admin routes second: %v", err)
	}
	if string(firstRoutes) != string(secondRoutes) {
		t.Fatalf("expected admin routes output to be stable")
	}
	if strings.Index(string(firstRoutes), "/system/alpha-article") > strings.Index(string(firstRoutes), "/system/zeta-article") {
		t.Fatalf("expected admin routes to be sorted, got:\n%s", string(firstRoutes))
	}
}

func TestRenderRegistryFilesCanExcludeModule(t *testing.T) {
	root := t.TempDir()
	if err := os.MkdirAll(filepath.Join(root, "internal/modules"), 0o755); err != nil {
		t.Fatalf("mkdir modules: %v", err)
	}
	writeMetaFile(t, root, "alpha_article", "/system/alpha-article", "AlphaArticlePage", "Alpha")
	writeMetaFile(t, root, "demo_article", "/system/demo-article", "DemoArticlePage", "Demo")

	backend, err := RenderBackendModulesFileWithOptions(root, nil, []string{"demo_article"})
	if err != nil {
		t.Fatalf("render backend with exclude: %v", err)
	}
	if strings.Contains(string(backend), "demo_article") {
		t.Fatalf("expected backend registry to exclude demo_article, got:\n%s", string(backend))
	}

	routes, err := RenderAdminRoutesFileWithOptions(root, nil, []string{"demo_article"})
	if err != nil {
		t.Fatalf("render routes with exclude: %v", err)
	}
	if strings.Contains(string(routes), "/system/demo-article") {
		t.Fatalf("expected routes to exclude demo_article, got:\n%s", string(routes))
	}
}
